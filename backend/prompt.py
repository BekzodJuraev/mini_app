def chat_system(message, context_data, history=None):
    """
    Оптимизированная система чата с поддержкой цветовых шкал здоровья
    и многоязычных ответов.
    Структура разделена на статичную и динамичную для работы Prompt Caching OpenAI.

    ВАЖНО: context_data стабилен на всю сессию диалога — должен строиться
    один раз в начале сессии и передаваться сюда без изменений на каждом
    сообщении. json.dumps с sort_keys=True гарантирует, что сериализация
    будет байт-в-байт идентичной при каждом вызове внутри сессии — это
    критично для срабатывания Prompt Caching (кэш рвётся при любом отличии
    в префиксе messages).
    """
    # 1. ПРАВИЛА ИНТЕРПРЕТАЦИИ ЦВЕТОВЫХ ШКАЛ ЗДОРОВЬЯ (health_system)
    # Синий (Топ / Отлично): 8.0 - 10.0
    # Желтый (Внимание / Среднее): 4.0 - 7.9
    # Красный (Опасность / Хуже всего): 0.0 - 3.9
    HEALTH_COLOR_RULES = """ПРАВИЛА ЦВЕТОВ ШКАЛ ЗДОРОВЬЯ (health_system):
Каждый показатель (score) строго привязан к цвету. Называй цвет утвердительно, без "если" и сослагательного наклонения.
8.0-10.0 = синяя зона (идеальное состояние). 4.0-7.9 = желтая зона (есть риски/умеренные отклонения). 0.0-3.9 = красная зона (критическое состояние).
Формат фразы: "Показатель находится в [цвет] зоне, так как [причина на основе данных, без предположений]."
Пример: score 6.5 у Зубочелюстной системы → "Показатель сейчас в желтой зоне, потому что [причина на основе возраста/рисков из данных]."."""

    # 2. СТАТИЧНЫЙ СИСТЕМНЫЙ ПРОМПТ (Идеально для кэша — никогда не меняется)
    SYSTEM_PROMPT = f"""Вы — продвинутый медицинский ИИ-ассистент. Вы знаете всё о здоровье пользователя и его питомцев на основе предоставленных данных.

ПРАВИЛА ОТВЕТА:
1. Отвечайте только на вопросы о здоровье человека, его теле, анализах, симптомах, лекарствах или здоровье его питомцев. На другие темы (фильмы, игры, политика) — вежливый отказ.
2. Отвечайте СТРОГО простым текстом, без Markdown (никаких **, #, маркеров списков).
3. Без переносов строк и спецсимволов форматирования — ответ идёт сплошным текстом из коротких предложений.
4. Отвечайте на том же языке, на котором задан вопрос пользователя.

{HEALTH_COLOR_RULES}
"""

    # sort_keys=True — защита от случайного изменения порядка ключей между
    # вызовами (иначе context_str может отличаться побайтово и сбросить кэш,
    # даже если данные не менялись)
    context_str = json.dumps(context_data, ensure_ascii=False, sort_keys=True)

    # 3. ФОРМИРУЕМ МАССИВ MESSAGES ДЛЯ СРАБАТЫВАНИЯ PROMPT CACHING
    # Постоянные правила и контекст идут ПЕРВЫМИ, история и новый вопрос — в самый конец.
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "system", "content": f"АКТУАЛЬНЫЕ ДАННЫЕ ИЗ БАЗЫ ДАННЫХ: {context_str}"}
    ]

    # Добавляем историю диалога (уже очищенную от пустых строк)
    if history:
        messages.extend(history)

    # Добавляем свежий вопрос пользователя
    messages.append({"role": "user", "content": message})

    # 4. ВЫЗОВ API (актуальный синтаксис для новых моделей OpenAI)
    response = openai.ChatCompletion.create(
        model="gpt-5-mini",
        messages=messages
    )

    # 5. ОЧИСТКА СТРОКИ ОТ ПЕРЕНОСОВ
    raw_content = response.choices[0].message.content
    clean_content = raw_content.replace("\n", " ").replace("\r", " ").strip()

    return clean_content


